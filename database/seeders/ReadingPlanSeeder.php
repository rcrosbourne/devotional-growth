    public function run(): void
    {
        $plan = ReadingPlan::query()->updateOrCreate(
            ['is_default' => true],
            [
                'name' => 'Read the Bible in a Year',
                'description' => 'A 365-day plan covering the entire Bible, from Genesis to Revelation.',
                'total_days' => 365,
            ],
        );

        if ($plan->days()->exists()) {
            return;
        }

        $days = $this->getDailyPassages();

        $records = [];
        $now = now();

        foreach ($days as $dayNumber => $passages) {
            $records[] = [
                'reading_plan_id' => $plan->id,
                'day_number' => $dayNumber,
                'passages' => json_encode($passages),
                'created_at' => $now,
                'updated_at' => $now,
            ];
        }

        foreach (array_chunk($records, 50) as $chunk) {
            ReadingPlanDay::query()->insert($chunk);
        }
    }

